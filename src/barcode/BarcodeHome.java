// Log handler
    private void logHandler() throws IOException
    {
        File logs = new File("logs.txt");
        if(logs.createNewFile())
        {
            System.out.println("Log File Missing :( \n File repaired \n Logs Maintainance Started");
            logHandler();
        }
        else
        {
            System.out.println("Log file Updating");
            logUpdate(logs.getPath());
        }
    }
// Log Updater
    private void logUpdate(String logPath) throws IOException
    {
        
        try (FileWriter updater = new FileWriter(logPath)) 
        {
            
            String prevData = logReader(logPath);
            String data = prevData+"\n"+valueText.getText();
            System.out.println(data);
            for(int i=0; i<data.length();i++)
            {
                updater.write(data.charAt(i));
            }
        }
        catch(IOException ex)
        {
            System.out.println("Error in file Update./n Either the file is mission or you dont have required permission for it");
        }
    }
    private String logReader(String logPath) throws FileNotFoundException , IOException
    {
        String prevData="";
        BufferedReader dataLines = new BufferedReader(new FileReader(new File(logPath)));
        String lines;
        while((lines=dataLines.readLine())!=null)
        {
            prevData = prevData.concat(lines); 
        }
        return prevData;
        
    }
    
    
