    private void openButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openButtonActionPerformed
        JFileChooser chooser = new JFileChooser();
        chooser.setCurrentDirectory(new java.io.File("."));
        chooser.setDialogTitle("Select Barcode Image");
        chooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        //File Extention Specified
        //......................................
        chooser.addChoosableFileFilter(new FileFilter() {

            @Override
            public String getDescription() {
                return "JPEG Image (*.jpeg)";
            }

            @Override
            public boolean accept(File f) {
                if (f.isDirectory()) {
                    return true;
                } else {
                    return f.getName().toLowerCase().endsWith(".jpeg");
                }
            }
        });
        //File Extention Specified

        chooser.addChoosableFileFilter(new FileFilter() {

            @Override
            public String getDescription() {
                return "PNG Image (*.png)";
            }

            @Override
            public boolean accept(File f) {
                if (f.isDirectory()) {
                    return true;
                } else {
                    return f.getName().toLowerCase().endsWith(".png");
                }
            }
        });
        chooser.setAcceptAllFileFilterUsed(false);

        if (chooser.showOpenDialog(null) == JFileChooser.APPROVE_OPTION) {
            ongoing.setText("Reading image...");
            openPath = chooser.getSelectedFile().toString();
            System.out.print(openPath);
            BarCodeReader brc = new BarCodeReader(openPath);
            while(brc.read())
            {
                encodeText.setText(brc.getCodeText());
                encodeTypeText.setText(brc.getCodeTypeName());
            }
            ImageIcon image = new ImageIcon(openPath);
            readView.setIcon(image);
            ongoing.setText("");
    }//GEN-LAST:event_openButtonActionPerformed
    }
